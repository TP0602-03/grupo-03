package ar.fiuba.tdd.tp.graph;

public class Coord {
    private final Integer x;
    private final Integer y;

    public Coord(Integer x, Integer y) {

        this.x = x;
        this.y = y;
    }

    public Integer getY() {
        return y;
    }

    public Integer getX() {
        return x;
    }
}
